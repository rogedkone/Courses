def is_palindrome(text):
    s, b = "", ""
    for i in text:
        if i.isalpha() or i.isdigit():
            s += i
    for i in text:
        if i.isalpha() or i.isdigit():
            b += i
    if b.lower() == s[::-1].lower():
        return "True"
    else:
        return "False"

